# Initialize the variable 'a' with the value 100
a = 100

# Start a while loop that continues as long as 'a' is less than or equal to 200
while a <= 200:
    # Print the current value of 'a'
    print(a)
    
    # Increase the value of 'a' by 5
    a += 5